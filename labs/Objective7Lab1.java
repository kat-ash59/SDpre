// need to be able to use import to get input variables
import java.time.*;
import java.util.*;

public class Objective7Lab1
{
	public static void main(String[] args)
	{
		// now declaring an integer with the value of the number 10
		int counter = 10;
		
		// printing out the counter from 10 to 1
		// using a do while loop
	
		
		while (counter > 0)
		{
			System.out.println("\n\tThe value of counter = " + counter + "\n");
			counter = counter - 1;
		}
		
	
	}
	
	
	
}
		
