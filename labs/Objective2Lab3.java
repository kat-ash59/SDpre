public class Objective2Lab3
{
		public static void main(String[] args)
	{
		// integer for the year i was born in
		int year = 1959;
		// string for the month i was born in
		String month = "July";
		// Is today a weekday or is it the weekend
		// since I am writing this on a Monday the value will be true since it is a weekday
		Boolean dayofweek = true;
		
		System.out.println("\n\n");
		// putting them all on the same line but adding newline and tab before each variable
		System.out.println("\tThe year I was born in is: " + year + "\n\tThe month I was born in is: " + month + "\n\tIs today a weekday (will be true) or is it the weekend (will be false): " + dayofweek);
		System.out.println("\n\n");
	}
}
		
